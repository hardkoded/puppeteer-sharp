namespace PuppeteerSharp
{
    /// <summary>
    /// Represents the state of a browser window.
    /// </summary>
    public enum WindowState
    {
        /// <summary>
        /// Normal window state.
        /// </summary>
        Normal,

        /// <summary>
        /// Minimized window state.
        /// </summary>
        Minimized,

        /// <summary>
        /// Maximized window state.
        /// </summary>
        Maximized,

        /// <summary>
        /// Fullscreen window state.
        /// </summary>
        Fullscreen,
    }
}
